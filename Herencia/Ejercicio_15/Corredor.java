public class Corredor {
    private int distanciaPreferida;
    private String tecnicaPreferida;

    public Corredor(
            int distanciaPreferida, 
            String tecnicaPreferida) {
        this.distanciaPreferida = distanciaPreferida;
        this.tecnicaPreferida = tecnicaPreferida;
    }

    public int getDistanciaPreferida() {
        return distanciaPreferida;
    }

    public String getTecnicaPreferida() {
        return tecnicaPreferida;
    }
    
    
    
    
    
}
